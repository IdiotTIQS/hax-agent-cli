// ── TemplateRegistry ─────────────────────────────────────────────────────────

class TemplateRegistry {
  /**
   * @param {object} [options]
   * @param {TemplateEngine} [options.engine] — inject a custom TemplateEngine
   */
  constructor(options = {}) {
    /** @type {TemplateEngine} */
    this._engine = options.engine || new TemplateEngine();

    /** @type {Map<string, object>} name -> template definition */
    this._templates = new Map();

    this._registerBuiltins();
  }

  // ---------------------------------------------------------------------------
  // Public API
  // ---------------------------------------------------------------------------

  /**
   * Register a template.
   *
   * @param {string} name         — unique template name
   * @param {object} templateDef  — { template, category?, description?, required? }
   * @param {string} [templateDef.template]     — template string with {{var}} syntax
   * @param {string} [templateDef.category="general"] — grouping category
   * @param {string} [templateDef.description=""]     — human-readable description
   * @param {string[]} [templateDef.required=[]]      — mandatory variable names
   * @returns {this}
   */
  register(name, templateDef) {
    if (!name || typeof name !== "string") {
      throw new TypeError("TemplateRegistry.register: name must be a non-empty string");
    }
    if (!templateDef || typeof templateDef !== "object") {
      throw new TypeError("TemplateRegistry.register: templateDef must be an object");
    }
    if (typeof templateDef.template !== "string") {
      throw new TypeError("TemplateRegistry.register: templateDef.template must be a string");
    }

    const def = {
      name,
      template: templateDef.template,
      category: templateDef.category || "general",
      description: templateDef.description || "",
      required: Array.isArray(templateDef.required) ? templateDef.required : [],
    };

    this._templates.set(name, def);
    return this;
  }

  /**
   * Generate output from a named template merged with the supplied variables.
   *
   * @param {string} name       — registered template name
   * @param {object} [variables={}] — key-value map for template substitution
   * @returns {string} Rendered output.
   */
  generate(name, variables = {}) {
    const def = this._templates.get(name);
    if (!def) {
      throw new Error(`TemplateRegistry.generate: unknown template "${name}"`);
    }
    return this._engine.compile(def.template, variables);
  }

  /**
   * List registered templates, optionally filtered by category.
   *
   * @param {string} [category] — if provided, only return templates in this category
   * @returns {Array<{ name: string, category: string, description: string, required: string[] }>}
   */
  listTemplates(category) {
    const result = [];
    for (const [, def] of this._templates) {
      if (category === undefined || def.category === category) {
        result.push({
          name: def.name,
          category: def.category,
          description: def.description,
          required: def.required,
        });
      }
    }
    return result.sort((a, b) => a.name.localeCompare(b.name));
  }

  /**
   * Validate that the supplied variables satisfy the template's required
   * fields.  Also runs the template engine's structural validation.
   *
   * @param {string} name       — registered template name
   * @param {object} [variables={}] — variables to check
   * @returns {{ valid: boolean, errors: string[], warnings: string[] }}
   */
  validate(name, variables = {}) {
    const def = this._templates.get(name);
    if (!def) {
      return { valid: false, errors: [`Unknown template: "${name}"`], warnings: [] };
    }

    const errors = [];
    const warnings = [];

    // Check required variables.
    for (const req of def.required) {
      const val = this._engine._resolve
        ? this._engine._resolve(req, variables)
        : variables[req];

      if (val === null || val === undefined || val === "") {
        errors.push(`Missing required variable: "${req}"`);
      }
    }

    // Structural validation of the template itself.
    const struct = this._engine.validateTemplate(def.template);
    if (!struct.valid) {
      errors.push(...struct.errors);
    }

    // Extract all referenced variables and warn about ones that are missing.
    const referenced = this._engine.extractVariables(def.template);
    for (const ref of referenced) {
      // Skip block keywords like "isLast", "item" which are injected by the engine.
      if (ref === "isLast" || ref === "item") continue;

      const val = this._engine._resolve
        ? this._engine._resolve(ref, variables)
        : variables[ref];

      if (val === null || val === undefined || val === "") {
        warnings.push(`Variable "${ref}" is used in the template but not provided`);
      }
    }

    return {
      valid: errors.length === 0,
      errors,
      warnings,
    };
  }

  /**
   * Remove a template from the registry.
   *
   * @param {string} name
   * @returns {boolean} true if removed, false if not found
   */
  unregister(name) {
    return this._templates.delete(name);
  }

  /**
   * Return the number of registered templates.
   *
   * @returns {number}
   */
  get size() {
    return this._templates.size;
  }

  /**
   * Return the list of all categories.
   *
   * @returns {string[]}
   */
  getCategories() {
    const cats = new Set();
    for (const [, def] of this._templates) {
      cats.add(def.category);
    }
    return [...cats].sort();
  }

  // ---------------------------------------------------------------------------
  // Private: built-in registration
  // ---------------------------------------------------------------------------

  /** @private */
  _registerBuiltins() {
    const builtins = [
      {
        name: "package.json",
        template: PACKAGE_JSON_TPL,
        category: BUILTIN_CATEGORY,
        description: "Node.js package.json manifest",
        required: ["name", "version"],
      },
      {
        name: "tsconfig.json",
        template: TSCONFIG_JSON_TPL,
        category: BUILTIN_CATEGORY,
        description: "TypeScript compiler configuration",
        required: [],
      },
      {
        name: ".env",
        template: DOTENV_TPL,
        category: BUILTIN_CATEGORY,
        description: "Environment variable file",
        required: [],
      },
      {
        name: "docker-compose",
        template: DOCKER_COMPOSE_TPL,
        category: BUILTIN_CATEGORY,
        description: "Docker Compose service definition",
        required: ["name"],
      },
      {
        name: "Makefile",
        template: MAKEFILE_TPL,
        category: BUILTIN_CATEGORY,
        description: "GNU Make build automation",
        required: ["name"],
      },
      {
        name: "github-actions",
        template: GITHUB_ACTIONS_TPL,
        category: BUILTIN_CATEGORY,
        description: "GitHub Actions CI workflow",
        required: ["workflowName", "jobName"],
      },
      {
        name: "gitlab-ci",
        template: GITLAB_CI_TPL,
        category: BUILTIN_CATEGORY,
        description: "GitLab CI/CD pipeline configuration",
        required: ["name"],
      },
    ];

    for (const b of builtins) {
      this._templates.set(b.name, b);
    }
  }
}

