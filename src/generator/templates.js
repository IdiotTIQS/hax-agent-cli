"use strict";

const { TemplateEngine } = require("../optimizer/template-engine");

/**
 * TemplateRegistry
 *
 * Manages a collection of named, categorised templates.  Provides registration,
 * generation (via the existing TemplateEngine), listing, and pre-generation
 * validation so callers can discover missing variables before rendering.
 *
 * Built-in templates cover common project files: package.json, tsconfig.json,
 * .env, docker-compose, Makefile, and CI pipelines (GitHub Actions / GitLab CI).
 */

// ── Template definition shape ────────────────────────────────────────────────
// Each registered template is an object:
// {
//   name:       string                                          // unique key
//   template:   string                                          // raw template text
//   category:   string                                          // grouping key
//   description: string                                         // human-readable
//   required:   string[]                                        // mandatory variables
// }

